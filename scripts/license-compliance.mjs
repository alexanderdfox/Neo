import { execSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { resolve } from "node:path";

const rootDir = process.cwd();
const targets = ["apps/api", "apps/web"];

const disallowedPatternStrings = (
  process.env.DISALLOWED_LICENSE_PATTERNS ??
  "GPL,AGPL,SSPL,Commons Clause,BUSL,UNLICENSED,UNKNOWN"
)
  .split(",")
  .map((s) => s.trim())
  .filter(Boolean);

const disallowedPatterns = disallowedPatternStrings.map((value) => ({
  label: value,
  regex: new RegExp(value, "i"),
}));

function collectLicenses(targetDir) {
  const raw = execSync("npx --yes license-checker --json --production", {
    cwd: resolve(rootDir, targetDir),
    stdio: ["ignore", "pipe", "pipe"],
    encoding: "utf8",
  });
  return JSON.parse(raw);
}

function splitLicenseValues(licenseValue) {
  if (!licenseValue) return ["UNKNOWN"];
  return String(licenseValue)
    .split(/\s+OR\s+|\s+AND\s+|,|\/|\|/i)
    .map((s) => s.trim())
    .filter(Boolean);
}

function formatNotices(sections) {
  const lines = [
    "# Third-Party Notices",
    "",
    "This file is auto-generated by `scripts/license-compliance.mjs`.",
    "Do not hand-edit; update dependencies and regenerate via CI.",
    "",
    `Generated at: ${new Date().toISOString()}`,
    "",
  ];

  for (const section of sections) {
    lines.push(`## ${section.name}`);
    lines.push("");
    lines.push("| Package | License | Repository |");
    lines.push("|---|---|---|");
    for (const dep of section.dependencies) {
      lines.push(
        `| \`${dep.packageKey}\` | ${dep.licenses ?? "UNKNOWN"} | ${
          dep.repository ? `[link](${dep.repository})` : "-"
        } |`
      );
    }
    lines.push("");
  }

  lines.push(
    "If a package requires additional attribution text, include it in release artifacts."
  );
  lines.push("");
  return lines.join("\n");
}

const sections = [];
const violations = [];

for (const target of targets) {
  const data = collectLicenses(target);
  const dependencies = Object.entries(data)
    .map(([packageKey, meta]) => ({
      packageKey,
      licenses: meta.licenses,
      repository: meta.repository ?? meta.url ?? "",
      target,
    }))
    .sort((a, b) => a.packageKey.localeCompare(b.packageKey));

  for (const dep of dependencies) {
    const licenseParts = splitLicenseValues(dep.licenses);
    for (const part of licenseParts) {
      for (const pattern of disallowedPatterns) {
        if (pattern.regex.test(part)) {
          violations.push({
            target: dep.target,
            packageKey: dep.packageKey,
            license: dep.licenses ?? "UNKNOWN",
            matched: pattern.label,
          });
        }
      }
    }
  }

  sections.push({ name: target, dependencies });
}

const noticePath = resolve(rootDir, "docs/THIRD_PARTY_NOTICES.md");
writeFileSync(noticePath, formatNotices(sections), "utf8");
console.log(`Updated ${noticePath}`);

if (violations.length > 0) {
  console.error("Disallowed license(s) detected:");
  for (const violation of violations) {
    console.error(
      `- [${violation.target}] ${violation.packageKey} -> ${violation.license} (matched: ${violation.matched})`
    );
  }
  process.exit(1);
}

console.log("License compliance check passed.");

